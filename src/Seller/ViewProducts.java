    private void loadProducts(){
        try(FileInputStream fis = new FileInputStream("products.dat");
              ObjectInputStream ois = new ObjectInputStream(fis)){
            
            List<ProductGetSet> products = (List<ProductGetSet>) ois.readObject();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            for(ProductGetSet product : products){
                if(product.getUsername().equals(username)){
                    model.addRow(new Object[]{
                        product.getUsername(),
                        product.getProductId(),
                        product.getProductName(),
                        product.getUnitPrice(),
                        product.getNumberOfUnits(),
                        product.getImagePath()

                    });
                }
            }
            
        }catch(IOException | ClassNotFoundException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading product");
        }
    }
    
    void refreshTableData(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        loadProducts();
        
        TableColumn imageColumn = jTable1.getColumnModel().getColumn(5);
        imageColumn.setCellRenderer(new ImageRenderer());
    }
    
